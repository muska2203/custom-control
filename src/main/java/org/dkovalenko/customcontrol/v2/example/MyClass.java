package org.dkovalenko.customcontrol.v2.example;

public class MyClass {

    private String name;

    private Integer years;

    public MyClass(String name, Integer years) {
        this.name = name;
        this.years = years;
    }

    public String getName() {
        return name;
    }

    public Integer getYears() {
        return years;
    }
}
